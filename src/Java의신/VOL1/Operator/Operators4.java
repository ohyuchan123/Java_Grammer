package Java의신.VOL1.Operator;

public class Operators4 {
    public static void main(String[] args) {
        int intValue1=1;
        int intValue2=2;

        System.out.println(intValue1>intValue2);
        System.out.println(intValue1<intValue2);

        System.out.println(intValue1>=intValue2);
        System.out.println(intValue1<=intValue2);
    }
}
