package Java의신.VOL1.Operator;

public class Operators3 {
    public static void main(String[] args) {
        int intValue = -10;
        int result = +intValue;
        System.out.println(result);
        result = -intValue;
        System.out.println(result);

        int intValue1=1;
        int intValue2=2;
        int intValue3=3;

        System.out.println(intValue1 == intValue2);
        System.out.println(intValue1 == intValue3);
        System.out.println(intValue1 != intValue2);
        System.out.println(intValue1 != intValue3);
    }
}
